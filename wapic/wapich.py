class WS_ITEM(Structure):
    _fields_=[('pData',POINTER(WS_BYTE)),('len', WS_ULONG)]

class WS_VERSION(Structure):
    _fields_=[('major', WS_BYTE), ('minor', WS_BYTE)]
    
class WS_INFO(Structure):
    _fields_=[('manufacturerID', WS_CHAR*32), ('description', WS_CHAR*32), \
              ('version', WS_VERSION)]
              
class WS_SESSION_INFO(Structure):
    _fields_=[('state', WS_SESSION_STATE), ('unitID', WS_UNIT_ID), ('userIDLen', WS_ULONG), \
              ('userID', WS_CHAR * WS_MAX_USER_LEN), ('authType', WS_AC_AUTH),\
              ('reserved', WS_FLAGS)]

class WS_USER_INFO(Structure):
    _fields_=[('role', WS_AC_ROLE), ('authType', WS_AC_AUTH), ('userIDLen', WS_ULONG), \
              ('userID', WS_CHAR * WS_MAX_USER_LEN), ('pinLen', WS_ULONG), \
              ('pin', WS_CHAR * WS_MAX_PIN_LEN)]

class WS_OBJ_ATTRIBUTE(Structure):
    _fields_=[('type', WS_BYTE), ('code', WS_ULONG), ('ulParamLen', WS_ULONG), \
              ('pParm', POINTER(WS_VOID))]
              
class WS_ATTRIBUTE(Structure):
    _fields_=[('type', WS_BYTE), ('code', WS_ULONG), ('ulParmLen', WS_ULONG), \
              ('pParm', POINTER(WS_VOID))]
              
class WS_TIME_INFO(Structure):
    _fields_=[('time', WS_ULONG), ('tz', WS_LONG)]

class WS_TRAP_INFO(Structure):
    _fileds_=[('addr', WS_CHAR), ('port', WS_USHORT)]

class WS_ALARM_INFO(Structure):
    _fields_=[('motion', WS_ULONG), ('temp', WS_ULONG), ('erase', WS_ULONG)]

class WS_LMS_INFO(Structure):
    _fields_=[('lmkPrimaryID',WS_BYTE*WS_MAX_LMK_ID), ('lmkPrimaruIDLen', WS_ULONG), \
              ('ulPrimaryK', WS_ULONG), ('ulPrimaryN', WS_ULONG),\
              ('lmkDormantID',WS_BYTE* WS_MAX_LMK_ID), \
              ('lmkDormantIDLen', WS_ULONG), \
              ('ulDormantK', WS_ULONG), ('ulDormantN', WS_ULONG)]

class WS_PORT_IP_INFO(Structure):
    _fields_=[('method', WS_ULONG), ('addr', WS_CHAR * WS_MAX_IP_ADDR_LEN), \
              ('mask', WS_CHAR* WS_MAX_IP_ADDR_LEN), \
              ('gw', WS_CHAR*WS_MAX_IP_ADDR_LEN)]

class WS_PORT_DESC(Structure):
    _fields_=[('port', WS_PORT_ID), ('desc', WS_CHAR * WS_MAX_LABEL)]
    
class WS_PORT_SERIAL_INFO(Structure):
    _fields_=[('port', WS_ULONG), ('configurable', WS_BOOL), ('baud', WS_ULONG), \
              ('data_len', WS_ULONG), ('stop_len', WS_ULONG), \
              ('flow_ctl', WS_ULONG), ('parity', WS_ULONG)]
              
class WS_PORT_CONSOLE_INFO(Structure):
    _fields_=[('port', WS_ULONG),('op', WS_CFG_OP), ('configurable', WS_BOOL)]

class WS_TEST_INFO(Structure):
    _fields_=[('testID', WS_TEST_ID), ('descLen', WS_ULONG), ('desc',WS_CHAR * WS_MAX_DESC)]

class WS_SW_LOAD_STATE(Structure):
    _fields_=[('state', WS_ULONG), ('stateDesc', WS_CHAR * 128), \
              ('code', WS_ULONG), ('workCompleted', WS_ULONG), \
              ('workToDo', WS_ULONG)]
              
class WS_LOG_ENTRY_INFO(Structure):
    _fields_=[('logType', WS_LOG_TYPE), ('entryLen', WS_ULONG), \
              ('entry', WS_CHAR * WS_MAX_LOG_ENTRY_LEN)]

